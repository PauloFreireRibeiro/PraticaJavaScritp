//Operadores aritmeticos - basicos da matemática

console.log(2+2);//soma
console.log(4-6);//subtração
console.log(50/10);//divisão
console.log(5*10);//multiplicação
console.log(15%3);//modular
console.log(4**2);//potenciação

//Operadores comparação 

console.log(4>2);//maior > ou menor <
console.log(10<=5);// maior igual >= ou menor igual <=
console.log(5==5);// igual simples
console.log(6!="6");// diferente simples
console.log(10!=="10");// diferente extrito

//Operadores lógicos

console.log(10>1 && 2==4);//E AND - quando ambas comparações são atendidas
console.log(5>2 || 7==7);// Ou OUR - quando apenas uma condição é atendida
console.log(!true);//Retorna o valor inverso do original

//Operador concatenador
var nome="Paulo";
var sobrenome="Freire";
console.log(nome+" "+sobrenome);//concatena (une) informações, dados
