//variavel "String" - Armazena texto ou palavras, obrigatorio entre "" ou ''
let nome="Paulo";
console.log(nome);

//variavel Number - armazena um valor númerico
let idade=22;
console.log(idade);

//variavel Boolean - devolve um valor true ou false
let casado=true;
console.log(casado);

//variavel Object - Cria um objeto
let ficha={
    raca:"Humano",
    poder:10,
    mana:5

};
console.log(ficha);

//variavel Array - Cria uma lista
let listaMercado=["carne","arroz","leite","banana"];
console.log(listaMercado);

//const - Uma const não pode ter seu valor alterado durante o codígo
const corFavorita="Azul";
console.log(corFavorita);